## Lietotājs ievada savu vērtējumu programmēšana izvadīt vārdisko vērtējumu (izcili, teicami utt.)

vertejums = int(input("Ievadiem savu vērtējumu: "))

if vertejums == 1:
    print("negatīvs vērtējums")
elif vertejums == 3:
    print("negatīvs vērtējums")
elif vertejums == 4:
    print("gandrīz viduvēji")
elif vertejums == 5:
    print("viduvēji")
elif vertejums == 6:
    print("gandrīz labi")
elif vertejums == 7:
    print("labi")
elif vertejums == 8:
    print("ļoti labi")
elif vertejums == 9:
    print("teicami")
elif vertejums == 10:
    print("izcili")